package kata.fizzbuzz

trait Fizzbuzz {
  def fizzbuzz(i: Int) : String = ""
}

trait Exercise {
  sef: Fizzbuzz =>
    /**
     * @param i an Integer >= 0
     * @return fizz if 3 is a divisor of i
     *         buzz if 5 is a divisor of i
     *         fizzbuzz if 3 and 5 is a divisor of i
     *         i otherwise
     */         
    override def fizzbuzz(i: Int) = ""
}